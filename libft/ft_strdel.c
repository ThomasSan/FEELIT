#include <stdlib.h>

void	ft_strdel(char **as)
{
	int	i;

	i = 0;
	if (as == NULL)
		return ;
	while (*as[i] != '\0')
	{
		*as[i] = '\0';
		i++;
	}
	free(*as);
	as[0] = NULL;
}
